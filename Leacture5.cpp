// Bitwise Operator and for Loops

#include <iostream>
using namespace std;
int main ()
{
  cout<<"using Bitwise Operator"<<endl;
  int a = 4;
  int b = 6;
  
  cout<<"a&b "<< (a&b) <<endl;
  cout<<"a|b "<< (a|b) <<endl;
  cout<<"~a"<< (~a) <<endl;
  cout<<"a^b "<< (a^b) <<endl;
  
  cout << "____________" <<endl;

  cout <<"Followings are left shift and right shift"<<endl;
  cout<<"17>>1 " <<(17>>1)<<endl;
  cout<<"17>>2 "<<(17<<2)<<endl;
  cout<<"19<<1 "<<(19<<1)<<endl;
  cout<<"21<<2 "<<(21<<2)<<endl;


  return 0;
}
